/**
 * Calculate module - see {@tutorial calculator-tutorial}
 * @module calculator
 */


/**
 * Add two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Sum of n1 and n2
 */
exports.add = (n1, n2) => n1 + n2;

/**
 * Multiply two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Product of n1 and n2
 */
exports.product = (n1, n2) => n1 * n2;

/**
 * Substract two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Subtract of n1 and n2
 */
exports.subtract = (n1, n2) => n1 - n2;

/**
 * Divide two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Division of n1 and n2
 */
exports.divide = (n1, n2) => n1 / n2;

